fn main(){
    //adiction
    let sum = 5 + 10;
    println!("Sum: {}", sum);

    //subtraction
    let difference = 95.5 - 4.3;
    println!("Difference: {}", difference);

    //multiplication
    let product = 4 * 30;
    println!("Product: {}",product);

    //divison
    let quotient = 56.7 / 32.2;
    let floored = 2/3; // Results in 0
    println!("Quotient: {}\nFloored: {}", quotient, floored);

    //remainder
    let remainder = 43 % 5;
    println!("Remainder: {}", remainder);
}